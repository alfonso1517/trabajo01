# Problema de estrategia en un partido de fútbol
# 
# Un equipo de fútbol está en los últimos 10 minutos de un partido muy cerrado y debe elegir una estrategia para atacar.
# El entrenador tiene tres alternativas tácticas, cada una con diferentes probabilidades de éxito dependiendo de lo que el equipo rival haga en esos momentos decisivos.
# 
# Estrategias disponibles:
#   1. Atacar por las bandas
# 2. Atacar por el centro
# 3. Jugar balones largos (pases directos a los delanteros)
# 
# Escenarios posibles:
#   Escenario 1: El equipo rival se queda con un jugador menos (expulsión)
# Escenario 2: El equipo rival juega a la defensiva y cierra los espacios
# Escenario 3: El equipo rival está buscando atacar y deja huecos en su defensa
# Escenario 4: El equipo rival presiona en todo el campo (presión alta)
# 
# Probabilidades de éxito para cada estrategia en cada escenario:
#   - Atacar por las bandas:
#   - Escenario 1: 85%
# - Escenario 2: 40%
# - Escenario 3: 80%
# - Escenario 4: 50%
# 
# - Atacar por el centro:
#   - Escenario 1: 50%
# - Escenario 2: 30%
# - Escenario 3: 90%
# - Escenario 4: 60%
# 
# - Jugar balones largos:
#   - Escenario 1: 60%
# - Escenario 2: 70%
# - Escenario 3: 70%
# - Escenario 4: 80%
# 
# Pregunta:
#   Si no se puede prever con certeza cuál será el escenario en los últimos 10 minutos y
# todos los escenarios son igualmente probables, ¿qué estrategia debería usar el entrenador para maximizar las probabilidades de éxito?